const provinceInput = document.getElementById("province-input");
const provinceOptions = document.querySelector(".province-options");

// Data provinsi dari variabel dataHarga, dengan menghapus "Semua Provinsi"
const provinsiData = dataHarga
    .filter(data => data.provinsi !== "Semua Provinsi")
    .map(data => data.provinsi);

// Menyembunyikan provinceOptions saat halaman dimuat
provinceOptions.style.display = "none";

// Fungsi untuk menampilkan pilihan provinsi sesuai inputan
function showProvinceOptions() {
    const searchTerm = provinceInput.value.toLowerCase();
    provinceOptions.innerHTML = '';

    const matchingProvinsi = provinsiData.filter(provinsi =>
        provinsi.toLowerCase().includes(searchTerm)
    );

    if (matchingProvinsi.length === 0 || provinceInput.disabled) {
        provinceOptions.style.display = "none";
        return;
    }

    for (const provinsi of matchingProvinsi) {
        const li = document.createElement("li");
        li.textContent = provinsi;
        li.addEventListener("click", () => {
            provinceInput.value = provinsi;
            provinceOptions.innerHTML = '';
            provinceOptions.style.display = "none";
        });
        provinceOptions.appendChild(li);
    }

    provinceOptions.style.display = "block";
}

// Mendengarkan perubahan pada input
provinceInput.addEventListener("input", showProvinceOptions);

// Mengatasi klik di luar input untuk menutup pilihan
document.addEventListener("click", (e) => {
    if (!provinceInput.contains(e.target) && e.target !== provinceOptions) {
        provinceOptions.innerHTML = '';
        provinceOptions.style.display = "none";
    }
});
